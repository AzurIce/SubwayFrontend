import pandas
from pprint import pprint

def split_list_by_first_char(lst):
    result = {}
    for item in lst:
        first_char = item[0]
        if first_char not in result:
            result[first_char] = []
        result[first_char].append(item)
    return result


df = pandas.read_csv('trips.csv')['shape_id'].dropna(axis='rows')
arr = sorted(map(str,list(set(df))))
print(arr)

